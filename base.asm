;GAME 
proc StartGame ;main code
	pusha
	mov [victory], 2
	mov [alreadyPlayed], 0
	
	game:
		call delay
		mov ax, 1h ;display mouse if another proc hid it
		int 33h
		
		cmp [victory], 2
		jne resultScreen
		
		mainScreen:
			mov ax, 2h
			int 33h
			mov [whichFile], 1 ;print filename1
			call printBMP 
			;display the mouse
			mov ax,1h
			int 33h
			call ButtonsOperation ;print main screen
			
			cmp [ExitButtonPressed], 0 ;true
			je exitGame ;exit button pressed. terminating program
			cmp [NewGameButtonPressed], 0 
			je mazeScreen ;jump to laoding screen while generating the maze
			cmp [HighScoresButtonPressed], 0 
			je creditScreen ;display high scores
			cmp [RulesButtonPressed], 0
			je rulesScreen ;display the rules
			
			jmp game
		creditScreen:
			call delay
			call printHighScores
			call delay			
			jmp game
		rulesScreen:
			call delay
			call printRulesScreen
			call delay
			jmp game
		mazeScreen:
			cmp [alreadyPlayed], 0
			jne printErrorMsg2
			call delay
			call gamePlay ;Y
			call delay
			jmp game
		resultScreen:
			call delay
			call results
			call delay
			mov [victory], 2
			mov [alreadyPlayed], 1 ;block
			jmp game
		printErrorMsg2:
			mov bp, seg erroemsg2
			mov es, bp
			mov bp, offset erroemsg2
			mov al, 0
			mov bh, 2h
			mov bl, 15
			mov cx, 32
			mov dh, 6
			mov dl, 5
			mov ah, 13h
			int 10h
			jmp game
	
	exitGame:
	popa
	ret
endp StartGame


;MAIN MAIN
proc ActualStart ;have whats in start but in a different proc for emulator
	mov ax, @data
	mov ds, ax

	;start program
	call clearRegs ;reset registers

	;Graphic mode
	mov ax, 13h
	int 10h

	mov ax, 0h ;reset the mouse
	int 33h
	
	;start the game forever
	call StartGame
	
	;terminate program
	;Back to text mode
	mov ah, 0
	mov al, 2
	int 10h
	
	;printing exit massages
	mov ax, 0900h
	mov dx, offset ExitMsg1
	int 21h
	mov dx, offset ExitMsg2
	int 21h
	
	;terminate program
	tLoop:
		xor ax, ax
		int 16h
		jz terminate
		jmp tLoop
	terminate:
		mov ax, 4C00h
		int 21h
		ret
endp ActualStart

start: ;start run
	
	call ActualStart

END start

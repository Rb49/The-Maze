;GAMEPLAY PROCS
proc gamePlay
	pusha
	
	mov ax, 2h ;hides mouse
	int 33h
	
	mov [whichFile], 2 ;loading screen
	call printBMP
	
	call generate
	
	mov [whichFile], 6 ;maze screen
	call printBMP
	
	call printMaze
	
	;paint edges
	mov bh,0h
	mov cx,[entranceX]
	mov dx,[entranceY]
	mov al,55
	mov ah,0ch
	int 10h
	mov [selectedX], cx
	mov [selectedY], dx
	dec cx
	mov al, 15
	int 10h
	mov cx, [exitX]
	mov dx, [exitY]
	inc cx
	int 10h
	mov cx, [exitX]
	mov dx, [exitY]
	mov al, 0
	int 10h
	
	mov [starterS], 0 ;timer value
	mov [formerS], 0
	
	ReadKeyboardLoop:  ;game play loop
	
		call updateTimer ;change timer
		
		;get mouse status  ;V
		mov ax,3h
		int 33h
		shr cx,1 ; adjust cx to range 0-319, to fit screen
		mov [mouseX], cx
		;sub dx, 1 ; move one pixel, so the pixel will not be hidden by mouse
		mov [mouseY], dx
		
		mov [whichButton], 1
		call switchFramesExit ;return is in the same place as the exit button
		
		cmp [bool1paintFrame], 0 ;if the mouse is in return button location
		jne checkForVictory
		mov ax, 3h
		int 33h
		cmp bx, 01h
		jne checkForVictory
		
		jmp gameFailed
	
		checkForVictory:
		
		cmp [selectedX], 300
		jne getInput
		cmp [selectedY], 180
		jne getInput
		;terminate game
		jmp endgame
		
		getInput:
	
		mov ax, 1h ;shows mouse
		int 33h
		;get input
		mov ah, 1
		int 16h
		jz ReadKeyboardLoop
		
		;hide mouse
		mov ax, 2h ;hides mouse
		int 33h
		
		mov ah, 0
		int 16h
		
		cmp ah, 17
		je MoveUp
		cmp ah, 30
		je MoveLeft
		cmp ah, 31
		je MoveDown
		cmp ah, 32
		je MoveRight	
    
		jmp ReadKeyboardLoop
	
	MoveUp:
		;check if moving is possible
		mov cx, [selectedX]
		mov dx, [selectedY]
		dec dx
		mov ah, 0Dh ;get pixel color
		int 10h
		cmp al, 15 ;trying to get on background
		je ReadKeyboardLoop

		mov ah, 0Ch
		mov al, 55
		int 10h
		dec [selectedY]
		inc dx
		mov al, 0 ;black
		int 10h
		jmp ReadKeyboardLoop
		
	MoveDown:
		;check if moving is possible
		mov cx, [selectedX]
		mov dx, [selectedY]
		inc dx
		mov ah, 0Dh ;get pixel color
		int 10h
		cmp al, 15 ;trying to get on background
		je ReadKeyboardLoop

		mov ah, 0Ch
		mov al, 55
		int 10h
		inc [selectedY]
		dec dx
		mov al, 0 ;black
		int 10h
		jmp ReadKeyboardLoop
	
	MoveLeft:
		;check if moving is possible
		mov cx, [selectedX]
		mov dx, [selectedY]
		dec cx
		mov ah, 0Dh ;get pixel color
		int 10h ;S
		cmp al, 15 ;trying to get on background
		je ReadKeyboardLoop

		mov ah, 0Ch
		mov al, 55
		int 10h
		dec [selectedX]
		inc cx
		mov al, 0 ;black
		int 10h
		jmp ReadKeyboardLoop
	
	MoveRight:
		;check if moving is possible
		mov cx, [selectedX]
		mov dx, [selectedY]
		inc cx
		mov ah, 0Dh ;get pixel color
		int 10h
		cmp al, 15 ;trying to get on background
		je ReadKeyboardLoop

		mov ah, 0Ch
		mov al, 55
		int 10h
		inc [selectedX]
		dec cx
		mov al, 0 ;black
		int 10h
		jmp ReadKeyboardLoop
	
	gameFailed:
		mov [victory], 0
		popa 
		ret
	endgame:
		mov [victory], 1
		popa
		ret
endp gamePlay


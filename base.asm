proc printMaze
	pusha
	
	call clearRegs
	
	mov si, offset board
	
	mov ah, 0Ch
	mov bh, 0h
	mov cx, 20
	mov dx, 40
	
	xor di, di
	printLoop:
		cmp di, [boardLength]
		jae printMazeEndLoop
		
		mov si, offset board
		add si, di
		cmp [byte ptr si], 0 ;frame
		je color15
		cmp [byte ptr si], 1 ;can be path
		je color15
		cmp [byte ptr si], 2 ;frontier
		je color0
		cmp [byte ptr si], 3 ;in
		je color0
		cmp [byte ptr si], 4 ;out
		je color15
		cmp [byte ptr si], 5 ;path
		je color0
		cmp [byte ptr si], 6 ;marked as neighbor
		je color0
		
		color15:
			mov al, 15
			jmp checkForLine
		color0:
			mov al, 0
			jmp checkForLine
			
		checkForLine:
			push ax bx dx ;F
			xor dx, dx
			mov ax, di
			mov bx, [mazeTotalX]
			div bx
			cmp dx, 0
			je incDX
			jmp noIncDX
		incDX:
			pop dx bx ax
			inc dx
			mov cx, 20
			inc di
			int 10h
			jmp printLoop
		noIncDX:
			pop dx bx ax
			inc cx
			inc di
			int 10h
			jmp printLoop
	
	printMazeEndLoop:
	popa
	ret
endp printMaze


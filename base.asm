proc paintFrame ;proc to color the button's frame
	cmp [whichButton], 1
	je exitFrame
	cmp [whichButton], 2
	je newgameFrame
	cmp [whichButton], 3
	je highscoresFrame
	cmp [whichButton], 4
	je rulesFrame
	jmp endPaintFrame
	
	exitFrame: ;frame is 300-315, 4-18
	mov [starterX], 300
	mov [finalX], 315
	mov [starterY], 4
	mov [finalY], 18
	jmp afterFrameAdj
	
	newgameFrame: ;frame is 108-217, 64-96
	mov [starterX], 108
	mov [finalX], 217
	mov [starterY], 64
	mov [finalY], 96
	jmp afterFrameAdj
	
	highscoresFrame: ;frame is 108-217, 106-138
	mov [starterX], 108
	mov [finalX], 217
	mov [starterY], 106
	mov [finalY], 138
	jmp afterFrameAdj
	
	rulesFrame: ;frame is 108-217, 147-179
	mov [starterX], 108
	mov [finalX], 217
	mov [starterY], 147
	mov [finalY], 179
	
	afterFrameAdj:
	
	;setting the paint settings
	xor bx, bx ;to prevent unwanted mistakes
	mov bh,0h
	mov al, [paintFrameColor] 
	mov ah,0Ch
	
	mov cx, [starterX]
	mov dx, [starterY]
	
	paintFrameLoop1: ;printing the higher row
		cmp cx, [finalX]
		je afterPaintFrameLoop1
		int 10h
		inc cx
		jmp paintFrameLoop1

	afterPaintFrameLoop1:
		mov cx, [starterX]
		mov dx, [finalY]
	
	paintFrameLoop2: ;printing the lower row
		cmp cx, [finalX]
		jg afterPaintFrameLoop2
		int 10h
		inc cx
		jmp paintFrameLoop2 ;E
	
	afterPaintFrameLoop2:
		mov cx, [starterX]
		mov dx, [starterY]
	
	paintFrameLoop3: ;printing the left column
		cmp dx, [finalY]
		je afterPaintFrameLoop3
		int 10h
		inc dx
		jmp paintFrameLoop3
	
	afterPaintFrameLoop3:
		mov cx, [finalX]
		mov dx, [starterY]
	
	paintFrameLoop4: ;printing the right column
		cmp dx, [finalY]
		je endPaintFrame
		int 10h
		inc dx
		jmp paintFrameLoop4
	
	endPaintFrame:
	
	ret
endp paintFrame


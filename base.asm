;TIMER PROC
proc updateClock
	pusha
	;reset runTime
	mov bx, offset runClock
	mov [byte ptr bx], 48 ;ascii for 0
	inc bx
	mov [byte ptr bx], 48 ;ascii for 0
	add bx, 2
	mov [byte ptr bx], 48 ;ascii for 0
	inc bx
	mov [byte ptr bx], 48 ;ascii for 0
	add bx, 2
	mov [byte ptr bx], 48 ;ascii for 0
	inc bx
	mov [byte ptr bx], 48 ;ascii for 0
	;read timer
	mov ax, 2C00h
	int 21h 
	mov [hou], ch
	mov [min], cl
	mov [sec], dh
	;take apart time into digits
	mov di, offset runClock
	mov bl, 10
	xor ah, ah
	mov al, [hou]
	div bl
	add [byte ptr di], al
	inc di
	add [byte ptr di], ah
	add di, 2
	xor ah, ah
	mov al, [min]
	div bl
	add [byte ptr di], al
	inc di
	add [byte ptr di], ah
	add di, 2
	xor ah, ah
	mov al, [sec]
	div bl
	add [byte ptr di], al
	inc di
	add [byte ptr di], ah
	
	mov ax, 2h ;hide mouse
	int 33h
	;print
	mov bp, seg runClock
	mov es, bp
	mov bp, offset runClock
	mov al, 0
	mov bh, 2h
	mov bl, 15
	mov cx, 8
	mov dh, 23
	mov dl, 31
	mov ah, 13h
	int 10h
	
	mov ax, 1h ;show mouse
	int 33h
	
	popa
	ret
endp updateClock

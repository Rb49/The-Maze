;SOLVEING PROCS
proc solve
	pusha
	;include edges
	mov bx, offset board
	add bx, [mazeTotalX]
	mov [byte ptr bx], 3
	mov bx, offset board
	add bx, [boardLength]
	inc bx
	sub bx, [mazeTotalX]
	mov [byte ptr bx], 3
	
	mov cx, [boardLength]
	dec cx
	mov si, [mazeTotalX]
	add si, [mazeTotalY]
	add si, [mazeTotalY]
	mov ax, 1
	solveL1:
		cmp ax, si
		jae solveEnd
		
		mov dx, 1
		solveL2:
			cmp dx, cx
			jae solveL1End
			mov [neighbors], 0
			L2_1:
				mov bx, offset board
				add bx, dx
				inc bx ;i+1
				cmp [byte ptr bx], 1
				jne L2_2
				inc [neighbors]
			L2_2:
				mov bx, offset board
				add bx, dx
				dec bx ;i-1
				cmp [byte ptr bx], 1
				jne L2_3
				inc [neighbors]
			L2_3:
				mov bx, offset board
				add bx, dx 
				add bx, [mazeTotalX] ;i+x
				cmp [byte ptr bx], 1
				jne L2_4
				inc [neighbors]
			L2_4:
				mov bx, offset board
				add bx, dx
				sub bx, [mazeTotalX] ;i-x
				cmp [byte ptr bx], 0
				jne L2_5
				inc [neighbors]
			L2_5:
				mov bx, offset board
				add bx, dx
				inc bx ;i+1
				cmp [byte ptr bx], 0
				jne L2_6
				inc [neighbors]
			L2_6:
				mov bx, offset board
				add bx, dx 
				dec bx ;i-1
				cmp [byte ptr bx], 0
				jne L2_7
				inc [neighbors]
			L2_7:
				mov bx, offset board
				add bx, dx
				add bx, [mazeTotalX] ;i+x
				cmp [byte ptr bx], 0
				jne L2_8
				inc [neighbors]
			L2_8:
				mov bx, offset board
				add bx, dx 
				sub bx, [mazeTotalX] ;i-x
				cmp [byte ptr bx], 1
				jne fillDE
				inc [neighbors]
				
			fillDE:
				cmp [neighbors], 3
				jb noFill
				;fill
					mov bx, offset board
					add bx, [mazeTotalX]
					
					mov di, offset board
					add di, dx
					;O
					cmp bx, di
					je noFill
					
					mov bx, offset board
					add bx, [boardLength]
					sub bx, 2
					sub bx, [mazeTotalX]
					
					mov di, offset board
					add di, dx
					
					cmp bx, di
					je noFill
					
					mov [byte ptr di], 0
					inc dx
					jmp solveL2
				
				noFill:
					inc dx
					jmp solveL2
					
		solveL1End:
		call printMaze
		inc ax
		jmp solveL1
	
	solveEnd:		
	popa
	ret
endp solve

